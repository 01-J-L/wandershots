def get_drive_service():
    """Authenticates using the OAuth User Token (token.json) to avoid Service Account quota limits."""
    if not os.path.exists(TOKEN_FILE):
        print(f"❌ ERROR: Token file not found at {TOKEN_FILE}.")
        raise ValueError(f"Token file not found at {TOKEN_FILE}. Please run auth_drive.py first.")

    try:
        # Load the credentials from the token.json file generated by auth_drive.py
        creds = Credentials.from_authorized_user_file(TOKEN_FILE, SCOPES)
        
        # Check if the token is expired and refresh it if necessary
        if creds and creds.expired and creds.refresh_token:
            creds.refresh(Request())
            # Save the refreshed token so it continues working indefinitely
            with open(TOKEN_FILE, 'w') as token:
                token.write(creds.to_json())
                
        service = build('drive', 'v3', credentials=creds)
        print(f"DEBUG: get_drive_service - Drive service built successfully via token.json") 
        return service
    except Exception as e:
        print(f"❌ Failed to load or build User OAuth credentials: {e}")
        # Re-raise to prevent None being passed down quietly
        raise RuntimeError(f"Google Drive service initialization failed: {e}")

def create_drive_folder(service, folder_name):
    """Creates a folder inside a pre-shared parent folder (typically a Shared Drive)."""
    PARENT_FOLDER_ID = os.environ.get("GOOGLE_DRIVE_PARENT_FOLDER_ID") 
    if not PARENT_FOLDER_ID:
        raise ValueError("Parent folder ID for Google Drive is not configured.")

    file_metadata = {
        'name': folder_name,
        'mimeType': 'application/vnd.google-apps.folder',
        'parents': [PARENT_FOLDER_ID]
    }
    print(f"DEBUG: create_drive_folder - Creating folder '{folder_name}' in parent '{PARENT_FOLDER_ID}' with supportsAllDrives=True") # Debug 3
    folder = service.files().create(body=file_metadata, fields='id, webViewLink', supportsAllDrives=True).execute()
    folder_id = folder.get('id')
    print(f"DEBUG: create_drive_folder - Created folder ID: {folder_id}, Link: {folder.get('webViewLink')}") 
    # Set permissions so anyone can view it. This relies on the Shared Drive's sharing settings.
    # If the Shared Drive itself restricts external sharing, this step might still fail or not work as expected.
    # For now, keep it, but be aware of Workspace admin settings.
    try:
        permission = {'type': 'anyone', 'role': 'reader'}
        service.permissions().create(fileId=folder_id, body=permission, supportsAllDrives=True).execute()
    except Exception as e:
        print(f"⚠️ Warning: Could not set 'anyone' permission for folder {folder_name}: {e}")
        print("This might be due to Google Workspace/Shared Drive sharing restrictions. The folder might still be created but not publicly viewable.")
    
    return folder_id, folder.get('webViewLink')

def upload_to_drive_with_retry(service, file_path, folder_id, filename):
    """Uploads file to GDrive, explicitly handling mimetypes and connection stability."""
    file_metadata = {'name': filename, 'parents': [folder_id]}
    
    mime_type, _ = mimetypes.guess_type(file_path)
    if mime_type is None:
        mime_type = 'application/octet-stream'
        
    media = MediaFileUpload(file_path, mimetype=mime_type, resumable=True) # <<< CONFIRM THIS IS TRUE
    
    for attempt in range(3):
        try:
            print(f"Uploading {filename}...")
            print(f"DEBUG: upload_to_drive_with_retry - Attempt {attempt+1}: File '{filename}', to folder '{folder_id}', MIME: '{mime_type}' with supportsAllDrives=True") # Debug 5
            print(f"DEBUG: upload_to_drive_with_retry - Local file exists: {os.path.exists(file_path)}") # Debug 6
            
            file = service.files().create(
                body=file_metadata,
                media_body=media,
                fields='id,name,webViewLink', # Request more fields to ensure a full response
                supportsAllDrives=True
            ).execute() 
            print(f"✅ Successfully uploaded {filename}! File ID: {file.get('id')}, View Link: {file.get('webViewLink')}") # Debug 7
            return file.get('id')
        except Exception as e:
            print(f"⚠️ Upload failed for {filename}. Retrying... ({attempt+1}/3)")
            print(f"🛑 EXACT ERROR: {str(e)}")
            time.sleep(2 ** attempt)
            
    return None

