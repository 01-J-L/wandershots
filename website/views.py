def process_gallery_upload(app, booking_id, saved_files):
    """Handles the heavy lifting of uploading files and cleaning up."""
    with app.app_context():
        booking = Booking.query.get(booking_id)
        if not booking: 
            print(f"DEBUG: process_gallery_upload - Booking ID {booking_id} not found. Aborting.")
            return

        service = None
        try:
            service = get_drive_service()
        except RuntimeError as e: # Catch the specific exception type raised in get_drive_service
            print(f"❌ process_gallery_upload - Service initialization failed: {e}")
            # Use current_app.logger for actual app logs, flash is for user-facing messages
            # flash("Gallery upload failed: Internal Google Drive service error. Please contact support.", 'error')
            # The flash message won't be seen as this is a background thread. Log instead.
            current_app.logger.error(f"Gallery upload failed for booking {booking_id}: Google Drive service init error: {e}")
            # Ensure local cleanup even if Drive fails completely
            for filepath, _ in saved_files:
                if os.path.exists(filepath):
                    os.remove(filepath)
            return # Abort processing if service isn't available

        # Now 'service' is guaranteed not to be None if we reach here, due to the raise in get_drive_service
        print(f"DEBUG: process_gallery_upload - Initialized service for booking {booking_id}") # Debug 8

        folder_id = None
        folder_link = None
        try:
            # Unique folder name per user/booking
            folder_name = f"Wandershots_Gallery_{booking.customer_name}_{uuid.uuid4().hex[:6]}"
            folder_id, folder_link = create_drive_folder(service, folder_name)

            if not folder_id:
                print(f"❌ process_gallery_upload - Failed to create Google Drive folder. Aborting file uploads.") # Debug 9
                current_app.logger.error(f"Gallery upload failed for booking {booking_id}: Could not create Google Drive folder.")
                # Ensure local cleanup even if folder creation failed
                for filepath, _ in saved_files:
                    if os.path.exists(filepath):
                        os.remove(filepath)
                return # Abort if folder creation failed

            print(f"DEBUG: process_gallery_upload - Folder created: {folder_id}, Link: {folder_link}") # Debug 10

            all_files_uploaded_successfully = True
            for filepath, original_name in saved_files:
                if not os.path.exists(filepath):
                    print(f"DEBUG: process_gallery_upload - ERROR: Local temp file does not exist: {filepath}. Skipping upload for this file.")
                    all_files_uploaded_successfully = False
                    continue # Skip this file but try others

                file_id = upload_to_drive_with_retry(service, filepath, folder_id, original_name)
                if not file_id:
                    print(f"❌ process_gallery_upload - File '{original_name}' could not be uploaded after retries.")
                    all_files_uploaded_successfully = False
                
                # STRICT CLEANUP: Always delete the temporary file after upload attempt
                if os.path.exists(filepath):
                    os.remove(filepath)
                    print(f"DEBUG: Deleted local temporary file: {filepath}")

            if not all_files_uploaded_successfully:
                print(f"⚠️ process_gallery_upload - Some files failed to upload for booking {booking_id}. Folder link will still be provided.")
                current_app.logger.warning(f"Gallery upload partial success for booking {booking_id}: Some files failed.")

            # Update Database and Notify User (only if folder was created)
            if folder_link:
                booking.gallery_link = folder_link
                db.session.commit()
                print(f"DEBUG: Database updated with gallery_link: {folder_link}")
                send_gallery_email(app, booking, folder_link)
                print(f"DEBUG: Gallery email triggered for {booking.customer_email}")
            else:
                print(f"❌ process_gallery_upload - No folder link available to update database or send email for booking {booking_id}.")
                current_app.logger.error(f"Gallery upload failed completely for booking {booking_id}: No folder link generated.")

        except Exception as e:
            print(f"❌ Critical Gallery Upload Error in background thread for booking {booking_id}: {e}")
            current_app.logger.critical(f"Gallery upload experienced critical error for booking {booking_id}: {e}", exc_info=True)
            # Ensure local cleanup even if Drive fails completely
            for filepath, _ in saved_files:
                if os.path.exists(filepath):
                    os.remove(filepath)
                    print(f"DEBUG: Deleted local temporary file after critical error: {filepath}")


